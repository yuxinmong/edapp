package com.ed.v1.util;

public class HalfToFullUtils {

	// 半角转全角，防止自动换行时混乱
	public static String halfToFull(String input) {
		char[] c = input.toCharArray();
		for (int i = 0; i < c.length; i++) {
			if (c[i] == 12288) {
				c[i] = (char) 32;
				continue;
			}
			if (c[i] > 65280 && c[i] < 65375)
				c[i] = (char) (c[i] - 65248);
		}
		return new String(c);
	}


}
